package com.irr310.i3d;

public class I3dVec2 {

    private final int width;
    private final int height;

    public I3dVec2(int width, int height) {
        this.width = width;
        this.height = height;
    }
    
    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

}
